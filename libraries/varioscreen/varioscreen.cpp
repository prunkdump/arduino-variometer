void ScreenElapsedTime::setBaseTime(int8_t* time) {

  for(uint8_t i = 0; i<3; i++) {
    baseTime[i] = time[i];
  }
}

void ScreenElapsedTime::setCurrentTime(int8_t* currentTime) {

  /* compute elapsed time */
  int8_t rem = 0;
  int8_t v;
  for(uint8_t i = 0; i<3; i++) {
    v = (currentTime[i] - baseTime[i]) - rem;
    if( v < 0 ) {
      v += 60;
      rem = 1;
    } else {
      rem = 0;
    }
    time[i] = v;
  }
}

