 int ScreenDigit::digitsBe4Decimal(double number)
{
  int cnt = 1;  // Always has one digit

  // Count -ve sign as one digit
  if(number < 0.0) {
    cnt++;
    number = -number;
  }

  // Count the number of digits beyond the 1st, basically, the exponent.
  while(number >= 10.0) {
    number /= 10;
    cnt++;
  }
  return cnt;
}


char * ScreenDigit::dtostrf2(double number, signed char width, unsigned char prec, char *s, boolean zero) 
{
    char *out;
    unsigned long long integer;
    double fraction, rounding;
    int digit, before, i;
    int delta;

    if (isnan(number)) {
        strcpy(s, "nan");
        return s;
    }
    if (isinf(number)) {
        strcpy(s, "inf");
        return s;
    }

    // rounding up to the precision
    rounding = 0.5;
    for (i = 0; i < prec; ++i)
        rounding /= 10.0;

    if (number < 0.0)
        number -= rounding;
    else
        number += rounding;

    out = s;
    before = digitsBe4Decimal(number);

    // check if padding is required
    if (width < 0) {
        delta = (-width) - (before + prec + 1);
        for (i = 0; i < delta; ++i) *out++ = ' ';
        width = 0;
    }

#ifdef IMU_DEBUG
    Serial.print("number  : ");
    Serial.print(number);
	Serial.print(" --  width  : ");
    Serial.print(width);
	Serial.print("  --  zero :");
    Serial.println(zero);
#endif //IMU_DEBUG
	
    // check if padding is required
    if ((width > 0) && (zero > 0)) {
        delta = width - (before + prec);
   	    if (prec > 0) delta -= 1; 
		if ((number < 0.0) ||  (plusDisplay)) delta -= 1; 
#ifdef IMU_DEBUG
		Serial.print("delta  : ");
        Serial.println(delta);
#endif //IMU_DEBUG
        for (i = 0; i < delta; ++i) {if (zero) {*out++ = '0';}}
    }

    // Handle negative numbers
    if (number < 0.0) {
      *out = '-';
      number = -number;
    }
	else if (plusDisplay) {
        *out = '+';
        ++out;		
	}

    // seperate integral and fractional parts
    integer = (unsigned long long) number;
    fraction = (double) (number - integer);

    // generate chars for each digit of the integral part
    i = before;
    while (integer >= 10) {
        digit = integer % 10;
        out[(i--) - 1] = '0' + digit;
        integer /= 10;
    }

    out[i - 1] = '0' + integer;
    out += before;

    if (prec) {
        // generate chars for each digit of the fractional part
        *out++ = '.';
        for (i = 0; i < prec; ++i) {
            fraction *= 10.0;
            digit = ((unsigned long long) fraction) % 10;
            *out++ = (char) ('0' + digit);
        }
    }


    *out = 0;
    return s;
}


